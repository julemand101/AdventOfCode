import 'package:advent_of_code/util.dart';
import 'package:advent_of_code_2024/day08.dart';
import 'package:test/test.dart';

final input = getInput(2024, 08).readAsLinesSync();

void main() {
  group('Part One', () {
    test('Example 1', () {
      expect(
        solveA(
          r'''
............
........0...
.....0......
.......0....
....0.......
......A.....
............
............
........A...
.........A..
............
............
'''
              .asLinesList,
        ),
        equals(14),
      );
    });
    test('Solution', () {
      expect(solveA(input), equals(423));
    });
  });
  group('Part Two', () {
    test('Example 1', () {
      expect(
        solveB(
          r'''
T.........
...T......
.T........
..........
..........
..........
..........
..........
..........
..........
'''
              .asLinesList,
        ),
        equals(9),
      );
    });
    test('Example 2', () {
      expect(
        solveB(
          r'''
............
........0...
.....0......
.......0....
....0.......
......A.....
............
............
........A...
.........A..
............
............
'''
              .asLinesList,
        ),
        equals(34),
      );
    });
    test('Solution', () {
      expect(solveB(input), equals(1287));
    });
  });
}
